public class sum<T> {
    public T soa, sob ; 

    public sum(T soa, T sob) {
        this.soa = soa;
        this.sob = sob;
    }
    
    public void setSoa(T soa) {
        this.soa = soa;
    }

    public void setSob(T sob) {
        this.sob = sob;
    }

    public T getSoa() {
        return soa;
    }

    public T getSob() {
        return sob;
    }
    
    
}
